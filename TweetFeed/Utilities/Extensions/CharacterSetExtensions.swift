import Foundation

public extension CharacterSet {
    
    public static func nonceCharacterSet() -> CharacterSet {
        return CharacterSet(charactersIn: "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ1234567890")
    }
    
}
